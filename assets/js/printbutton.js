// PRINT REQUIREMENTS FUNCTIONALITY

function artReqButton() {
  let printRequirements = document.getElementById('art-requirements');
  let minOrder = document.getElementById('min-order');
  let setUp = document.getElementById('setup-fees');
  let turnAround = document.getElementById('turnaround-time');
  let shirtStyles = document.getElementById('shirt-styles');
  let priceDown = document.getElementById('price-down');

  if (printRequirements.classList.contains('fa-plus')) {
    printRequirements.classList.remove("fa-plus");
    printRequirements.classList.add("fa-minus");
  } else {
    printRequirements.classList.remove("fa-minus");
    printRequirements.classList.add("fa-plus");
  } if(minOrder.classList.contains('fa-minus')) {
    minOrder.classList.remove('fa-minus');
    minOrder.classList.add('fa-plus');
  } if(setUp.classList.contains('fa-minus')) {
    setUp.classList.remove('fa-minus');
    setUp.classList.add('fa-plus');
  } if(turnAround.classList.contains('fa-minus')) {
    turnAround.classList.remove('fa-minus');
    turnAround.classList.add('fa-plus');
  } if(shirtStyles.classList.contains('fa-minus')) {
    shirtStyles.classList.remove('fa-minus');
    shirtStyles.classList.add('fa-plus');
  } if(priceDown.classList.contains('fa-minus')) {
    priceDown.classList.remove('fa-minus');
    priceDown.classList.add('fa-plus');
  }
}


